
let color = 'black';

switch(color) {

    case 'red': 
        console.log('color is red');
        break;
    case 'blue': 
        console.log('color is blue');
        break;
    case 'orange': 
        console.log('color is orange');
        break;
    case 'white': 
        console.log('color is white');
        break;
    case 'black': 
        console.log('color is black');
        break;

    default:
        console.log('color i unknown');
        break;

}
